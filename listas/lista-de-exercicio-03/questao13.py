N = int (input ('Digite um numero de 1 a 7: '))
if N == 1:
    print ('1-Domingo')
elif N == 2: 
    print ('2-Segunda')
elif N == 3:
    print ('3-Terça')
elif N == 4:
    print ('4-Quarta')
elif N == 5:
    print ('5-Quinta')
elif N == 6:
    print ('6-Sexta')
elif N == 7:
    print ('7-Sábado')
else:
    print ('Número inválido.')